
async function attemptLogin(payload, baseUrl) {
  // console.log(`${baseUrl}/Login/GetLoginEmployeeData`);
  const formBody = JSON.stringify(payload, (key, value) => {
    if (value !== null) {
      return value;
    }
    return {};
  });

  const response = await fetch(`${baseUrl}/Login/GetLoginEmployeeData`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: formBody,
  });
  const responseJson = await response.json();
  return responseJson;
}

export { attemptLogin };
